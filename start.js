// Проверяем, запущен ли скрипт в интерактивном терминале (TTY).
// Это ключевая проверка, которая решает всю проблему.
if (!process.stdout.isTTY) {
  console.error(
    "\x1b[31m%s\x1b[0m", // Красный цвет
    "Критическая ошибка: Это приложение должно быть запущено в интерактивном терминале (TTY).\n" +
      "Похоже, оно запущено в среде, где размеры окна определить невозможно."
  );
  process.exit(1);
}

// Регистрируем ts-node, чтобы он "на лету" компилировал наш TypeScript.
// Это позволяет нам запускать .ts файлы напрямую через Node.js.
require("ts-node").register();

// Запускаем наше основное приложение.
require("./src/index.ts");
